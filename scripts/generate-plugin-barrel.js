const fs = require('fs');
const path = require('path');

const pluginsDir = path.join(__dirname, '..', 'plugins');
const outputFile = path.join(pluginsDir, 'generated-plugin-registry.ts');

function toSafeId(id) {
  return id.replace(/[^a-zA-Z0-9_$]/g, '_');
}

function discoverPlugins() {
  const entries = [];

  for (const dirent of fs.readdirSync(pluginsDir, { withFileTypes: true })) {
    if (!dirent.isDirectory()) continue;

    const manifestPath = path.join(pluginsDir, dirent.name, 'manifest.json');
    if (!fs.existsSync(manifestPath)) continue;

    let manifest;
    try {
      manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf-8'));
    } catch {
      continue;
    }

    if (!manifest.id || manifest.enabled === false) continue;

    let pluginFile = null;
    if (manifest.frontend) {
      const relPath = manifest.frontend.plugin || manifest.frontend.entry;
      if (relPath) {
        const clean = relPath.replace(/\.ts$/, '').replace(/^\.\//, '');
        pluginFile = `./${dirent.name}/${clean}`;
      }
    }
    if (!pluginFile) {
      const frontendDir = path.join(pluginsDir, dirent.name, 'frontend');
      if (fs.existsSync(frontendDir)) {
        const files = fs.readdirSync(frontendDir).filter(f => f.endsWith('.plugin.ts'));
        if (files.length > 0) {
          const preferred = files.find(f => f.includes(dirent.name));
          pluginFile = `./${dirent.name}/frontend/${(preferred || files[0]).replace(/\.ts$/, '')}`;
        }
      }
    }

    if (!pluginFile) {
      console.warn(`  [skip] ${dirent.name}: no frontend .plugin.ts found`);
      continue;
    }

    entries.push({ id: manifest.id, pluginFile, safeId: toSafeId(manifest.id) });
  }

  return entries;
}

const entries = discoverPlugins();

let content = '// Auto-generated by scripts/generate-plugin-barrel.js\n';
content += '// Do not edit manually. Re-run `npm run generate-plugin-barrel` after adding/removing plugins.\n\n';
content += `// ${entries.length} bundled plugin(s) discovered\n`;
content += 'import { registerPluginEntry } from \'../src/app/plugin/services/plugin-import-registry\';\n';

for (const entry of entries) {
  content += `import { register as __${entry.safeId}Register } from '${entry.pluginFile}';\n`;
}

content += '\n';

for (const entry of entries) {
  content += `registerPluginEntry('${entry.id}', __${entry.safeId}Register);\n`;
}

fs.writeFileSync(outputFile, content);

console.log(`Generated generated-plugin-registry.ts with ${entries.length} entries`);
for (const entry of entries) {
  console.log(`  ${entry.id} → ${entry.pluginFile}`);
}
